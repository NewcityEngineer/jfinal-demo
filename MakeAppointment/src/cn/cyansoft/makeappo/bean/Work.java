package cn.cyansoft.makeappo.bean;

public class Work {


	
	private int id;
	private String agreementID;
	private String materielInfoID;
	private String storageRecord;
	private String amount;
	private String storageLoc;
	private String date;
	private String billPhoto;
	private String originalTime;
	private String makeup;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAgreementID() {
		return agreementID;
	}
	public void setAgreementID(String agreementID) {
		this.agreementID = agreementID;
	}
	public String getMaterielInfoID() {
		return materielInfoID;
	}
	public void setMaterielInfoID(String materielInfoID) {
		this.materielInfoID = materielInfoID;
	}
	public String getStorageRecord() {
		return storageRecord;
	}
	public void setStorageRecord(String storageRecord) {
		this.storageRecord = storageRecord;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getStorageLoc() {
		return storageLoc;
	}
	public void setStorageLoc(String storageLoc) {
		this.storageLoc = storageLoc;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getBillPhoto() {
		return billPhoto;
	}
	public void setBillPhoto(String billPhoto) {
		this.billPhoto = billPhoto;
	}
	public String getOriginalTime() {
		return originalTime;
	}
	public void setOriginalTime(String originalTime) {
		this.originalTime = originalTime;
	}
	public String getMakeup() {
		return makeup;
	}
	public void setMakeup(String makeup) {
		this.makeup = makeup;
	}
	
	
}
