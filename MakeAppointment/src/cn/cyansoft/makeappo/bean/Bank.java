package cn.cyansoft.makeappo.bean;

public class Bank {

	
	private int id;
	private String name;
	private String president;
	private String prePhone;
	private String duty;
	private String dutyName;
	private String businessMan;
	private String busiManPhone;
	private String address;
	private String remark;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPresident() {
		return president;
	}
	public void setPresident(String president) {
		this.president = president;
	}
	public String getPrePhone() {
		return prePhone;
	}
	public void setPrePhone(String prePhone) {
		this.prePhone = prePhone;
	}
	public String getDuty() {
		return duty;
	}
	public void setDuty(String duty) {
		this.duty = duty;
	}
	public String getDutyName() {
		return dutyName;
	}
	public void setDutyName(String dutyName) {
		this.dutyName = dutyName;
	}
	public String getBusinessMan() {
		return businessMan;
	}
	public void setBusinessMan(String businessMan) {
		this.businessMan = businessMan;
	}
	public String getBusiManPhone() {
		return busiManPhone;
	}
	public void setBusiManPhone(String busiManPhone) {
		this.busiManPhone = busiManPhone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
}
